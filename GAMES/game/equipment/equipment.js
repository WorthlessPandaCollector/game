class Equipment {
    constructor(name, hp, mp, str, int, dex, mag, atk, def, speed) {
        this.name = name;
        this.hp = hp;
        this.mp = mp;
        this.str = str;
        this.int = int;
        this.dex = dex;
        this.mag = mag;
        this.atk = atk;
        this.def = def;
        this.speed = speed;
    }
}


module.exports = Equipment;